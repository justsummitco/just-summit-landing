import { readdir, readFile } from "node:fs/promises";
import path from "node:path";

const rootDir = process.cwd();
const scanRoots = ["app", "components", "content", "lib"];
const allowedExtensions = new Set([".ts", ".tsx", ".js", ".jsx", ".md", ".mdx"]);

const bannedPatterns = [
  {
    label: "internal SEO wording",
    pattern: /\b(?:the practical\s+)?SEO questions?\b/i,
  },
  {
    label: "customer-visible keyword scaffolding",
    pattern: /\b(?:primary|target|seo)\s+keyword\b/i,
  },
  {
    label: "internal product-direction wording",
    pattern: /\bintended product direction\b/i,
  },
  {
    label: "patent/scaffold wording",
    pattern: /\bnarrowing the final\b/i,
  },
  {
    label: "implementation-scaffold wording",
    pattern: /\bimplementation goals?\b/i,
  },
  {
    label: "implementation-scaffold wording",
    pattern: /\bfinal hardware,\s*firmware,\s*or app implementation\b/i,
  },
  {
    label: "internal copy-review heading",
    pattern: /\bResponsible language while the product is developing\b/i,
  },
  {
    label: "internal build-log wording",
    pattern: /\bprivacy architecture notes?\b/i,
  },
  {
    label: "internal patent/scaffold wording",
    pattern: /\bplanned embodiments?\b/i,
  },
  {
    label: "internal implementation wording",
    pattern: /\bThe exact implementation depends\b/i,
  },
  {
    label: "AI/tooling leak",
    pattern: /\b(?:ChatGPT|AI-generated|generated by AI)\b/i,
  },
  {
    label: "placeholder media prompt",
    pattern: /\[\s*(?:founder portrait|in-use photo|companion app screen|product photo|image placeholder|photo placeholder)[^\]]*\]/i,
  },
  {
    label: "placeholder text",
    pattern: /\blorem ipsum\b/i,
  },
];

const findings = [];

async function walk(directory) {
  const entries = await readdir(directory, { withFileTypes: true });

  for (const entry of entries) {
    const fullPath = path.join(directory, entry.name);

    if (entry.isDirectory()) {
      await walk(fullPath);
      continue;
    }

    if (!entry.isFile() || !allowedExtensions.has(path.extname(entry.name))) {
      continue;
    }

    await auditFile(fullPath);
  }
}

async function auditFile(filePath) {
  const content = await readFile(filePath, "utf8");
  const lines = content.split(/\r?\n/);

  lines.forEach((line, index) => {
    bannedPatterns.forEach(({ label, pattern }) => {
      if (pattern.test(line)) {
        findings.push({
          file: path.relative(rootDir, filePath),
          line: index + 1,
          label,
          text: line.trim(),
        });
      }
    });
  });
}

for (const scanRoot of scanRoots) {
  await walk(path.join(rootDir, scanRoot));
}

if (findings.length > 0) {
  console.error("Public copy audit failed. Remove internal/scaffold wording before shipping.\n");
  findings.forEach((finding) => {
    console.error(`${finding.file}:${finding.line} - ${finding.label}`);
    console.error(`  ${finding.text}`);
  });
  process.exit(1);
}

console.log("Public copy audit passed.");
